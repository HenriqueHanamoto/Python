# Exercício 1: Contagem de Vogais
# Escreva um programa que peça ao usuário para inserir uma string e
# em seguida, conte o número de vogais (a, e, i, o, u) nessa string.

# Exercício 2: Reversão de String
# Peça ao usuário para inserir uma string e, em seguida, imprima a string de trás para frente.

# Exercício 3: Palíndromo
# Peça ao usuário para inserir uma palavra e determine se é um palíndromo 
# (uma palavra que é a mesma se lida de trás para frente, como "radar" ou "level").

# Exercício 4: Capitalização de Texto
# Peça ao usuário para inserir uma frase e, em seguida, imprima a frase
# com todas as palavras começando com letras maiúsculas.

# Exercício 5: Substituição de Substring
# Peça ao usuário para inserir uma string e, em seguida, 
# substitua todas as ocorrências de uma substring específica por outra substring. 
# Por exemplo, substitua todas as ocorrências de "gato" por "cachorro".